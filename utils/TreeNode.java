/**
 * @program: PACKAGE_NAME
 * @description: Definition for a binary tree node.
 * @author: w15021
 * @create: 2023-10-27
 **/
 public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;
    TreeNode() {}
    TreeNode(int val) { this.val = val; }
    TreeNode(int val, TreeNode left, TreeNode right) {
      this.val = val;
      this.left = left;
      this.right = right;
    }
}